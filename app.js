function initMealScanner() {
  const dropZone = document.getElementById('scanner-dropzone');
  const fileInput = document.getElementById('scanner-file-input');
  const actionContainer = document.getElementById('scanner-action-container');
  const resultCard = document.getElementById('scanner-results-card');
  const chipContainer = document.getElementById('scanner-preset-chips');

  if (!dropZone || !fileInput || !actionContainer || !resultCard) return;

  // Handle preset chips click
  if (chipContainer) {
    chipContainer.querySelectorAll('.preset-chip').forEach(chip => {
      chip.addEventListener('click', () => {
        const foodType = chip.getAttribute('data-food');
        
        // Setup a mock scanned state
        showScannedPreview(`images/presets/${foodType}.png`, `${foodType}_preset_meal.png`);
      });
    });
  }

  // Trigger file dialog
  dropZone.addEventListener('click', (e) => {
    if (e.target.closest('#scanner-remove-preview-btn') || e.target.closest('.btn')) {
      return; // Ignore if clicking remove/action buttons
    }
    fileInput.click();
  });

  // Drag over / leave styling
  dropZone.addEventListener('dragover', (e) => {
    e.preventDefault();
    dropZone.classList.add('dragover');
  });

  dropZone.addEventListener('dragleave', () => {
    dropZone.classList.remove('dragover');
  });

  dropZone.addEventListener('drop', (e) => {
    e.preventDefault();
    dropZone.classList.remove('dragover');
    
    if (e.dataTransfer.files && e.dataTransfer.files[0]) {
      handleUploadedFile(e.dataTransfer.files[0]);
    }
  });

  fileInput.addEventListener('change', () => {
    if (fileInput.files && fileInput.files[0]) {
      handleUploadedFile(fileInput.files[0]);
    }
  });
}

function handleUploadedFile(file) {
  const reader = new FileReader();
  reader.onload = (e) => {
    showScannedPreview(e.target.result, file.name);
  };
  reader.readAsDataURL(file);
}

