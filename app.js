// Active workout overlay launcher
function launchActiveWorkout(dayData) {
  const overlay = document.getElementById('active-workout-panel');
  const timerLabel = document.getElementById('active-timer-label');
  const titleEl = document.getElementById('active-workout-title');
  const exercisesContainer = document.getElementById('active-exercises-list');

  if (!overlay || !timerLabel || !titleEl || !exercisesContainer) return;

  appState.activeWorkout = {
    title: dayData.name,
    exercises: dayData.exercises.map(ex => ({ ...ex, completed: false })),
    startTime: new Date().toISOString()
  };

  titleEl.textContent = dayData.name;
  overlay.classList.add('active');

  // Load exercises list
  renderActiveExercises();

  // Start stopwatch
  startWorkoutTimer((formattedTime) => {
    timerLabel.textContent = formattedTime;
  });

  // Event handlers for action buttons
  const quitBtn = document.getElementById('active-quit-btn');
  const completeBtn = document.getElementById('active-complete-btn');

  // Single-use actions to prevent duplicates
  quitBtn.onclick = () => {
    if (confirm("Are you sure you want to stop the workout? Progress will not be saved.")) {
      stopWorkoutTimer();
      appState.activeWorkout = null;
      overlay.classList.remove('active');
    }
  };

  completeBtn.onclick = () => {
    const mins = stopWorkoutTimer();
    const exCount = appState.activeWorkout.exercises.filter(ex => ex.completed).length;
    
    // Add to completed list
    appState.workouts.push({
      title: appState.activeWorkout.title,
      exercisesCount: exCount,
      durationMinutes: mins,
      timestamp: new Date().toISOString()
    });

    overlay.classList.remove('active');
    appState.activeWorkout = null;
    saveState();
    updateUI();

    // Trigger celebration dialog modal
    showCongratsModal(mins, exCount);
  };
}

