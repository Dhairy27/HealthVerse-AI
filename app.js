// --- HEALTHVERSE AI STATE MANAGEMENT ---

const DEFAULT_STATE = {
  profile: null, // User details
  dailyIntake: {
    calories: 0,
    protein: 0,
    carbs: 0,
    fats: 0,
    lastReset: null // Reset daily macros on next day
  },
  meals: [], // { name: '', calories: 0, protein: 0, carbs: 0, fats: 0, timestamp: Date, image: '' }
  workouts: [], // { title: '', exercisesCount: 0, durationMinutes: 0, timestamp: Date }
  weightLogs: [], // { weight: 0, date: 'YYYY-MM-DD' }
  customRoutine: null, // Generated by AI
  activeWorkout: null, // Current workout progress
  theme: 'light' // Theme mode: 'light' or 'dark'
};

let appState = { ...DEFAULT_STATE };

// Load State from LocalStorage
function loadState() {
  const data = localStorage.getItem('healthverse_state');
  if (data) {
    try {
      appState = JSON.parse(data);
      if (!appState.theme) {
        appState.theme = 'light';
      }
      checkDailyReset();
    } catch (e) {
      console.error("Error parsing stored state, resetting to default.", e);
      appState = { ...DEFAULT_STATE };
    }
  } else {
    // Fresh run: Seed some sample logs to make charts immediately impressive
    appState = { ...DEFAULT_STATE };
    seedSampleData();
  }
}

// Save State to LocalStorage
function saveState() {
  localStorage.setItem('healthverse_state', JSON.stringify(appState));
}

// Theme application
function applyTheme() {
  const isDark = appState.theme === 'dark';
  if (isDark) {
    document.body.classList.add('dark-theme');
  } else {
    document.body.classList.remove('dark-theme');
  }
}

// Initialize Theme Toggle button listeners
function initThemeToggle() {
  const toggleBtn = document.getElementById('theme-toggle-btn');
  if (!toggleBtn) return;
  
  toggleBtn.addEventListener('click', () => {
    appState.theme = appState.theme === 'dark' ? 'light' : 'dark';
    saveState();
    applyTheme();
    
    // Re-render charts if analytics panel is open
    const activePanel = document.querySelector('.view-panel.active');
    if (activePanel && activePanel.id === 'analytics-panel') {
      renderCharts();
    }
  });
}

// Reset daily numbers if it's a new day
function checkDailyReset() {
  const todayStr = new Date().toDateString();
  if (appState.dailyIntake.lastReset !== todayStr) {
    appState.dailyIntake.calories = 0;
    appState.dailyIntake.protein = 0;
    appState.dailyIntake.carbs = 0;
    appState.dailyIntake.fats = 0;
    appState.dailyIntake.lastReset = todayStr;
    saveState();
  }
}

// Seed sample progress data if no profile is set yet
function seedSampleData() {
  const now = new Date();
  
  // Weights (last 7 days)
  appState.weightLogs = [];
  for (let i = 6; i >= 0; i--) {
    const d = new Date(now);
    d.setDate(now.getDate() - i);
    appState.weightLogs.push({
      weight: 78.5 - (i * 0.15) + (Math.sin(i) * 0.1),
      date: d.toISOString().split('T')[0]
    });
  }

  // Sample Meals
  appState.meals = [
    { name: "Avocado Toast with Egg", calories: 380, protein: 14, carbs: 42, fats: 18, timestamp: new Date(now - 3 * 3600000).toISOString() },
    { name: "Chicken and Brown Rice", calories: 520, protein: 42, carbs: 55, fats: 12, timestamp: new Date(now - 8 * 3600000).toISOString() }
  ];

  // Sample Workouts
  appState.workouts = [
    { title: "Upper Body Hypertrophy", exercisesCount: 5, durationMinutes: 48, timestamp: new Date(now - 24 * 3600000).toISOString() },
    { title: "HIIT Core Conditioning", exercisesCount: 6, durationMinutes: 30, timestamp: new Date(now - 48 * 3600000).toISOString() }
  ];

  appState.dailyIntake.lastReset = now.toDateString();
}

// Reset ALL State data
function resetAllData() {
  localStorage.removeItem('healthverse_state');
  appState = { ...DEFAULT_STATE };
  seedSampleData();
  saveState();
  window.location.reload();
}


// --- FORMULAS & TARGET CALCULATIONS ---

function calculateMetrics(age, gender, height, weight, activityLevel, goal) {
  // BMI = kg / m^2
  const heightMeters = height / 100;
  const bmi = weight / (heightMeters * heightMeters);

  // BMR (Mifflin-St Jeor Equation)
  let bmr = 0;
  if (gender === 'male') {
    bmr = 10 * weight + 6.25 * height - 5 * age + 5;
  } else {
    bmr = 10 * weight + 6.25 * height - 5 * age - 161;
  }

  // TDEE (Total Daily Energy Expenditure) multiplier
  const activityMultipliers = {
    sedentary: 1.2,
    light: 1.375,
    moderate: 1.55,
    active: 1.725,
    extreme: 1.9
  };
  const tdee = bmr * (activityMultipliers[activityLevel] || 1.2);

  // Calorie Target based on goal
  let targetCalories = tdee;
  if (goal === 'lose_fat') {
    targetCalories = tdee - 500; // Deficit
    if (targetCalories < 1200) targetCalories = 1200; // Health safety limit
  } else if (goal === 'gain_muscle') {
    targetCalories = tdee + 350; // Surplus
  } else if (goal === 'endurance') {
    targetCalories = tdee + 250;
  }

  // Macro Distributions (percentage of calories)
  let pPct = 0.25, cPct = 0.50, fPct = 0.25; // Default Balanced
  
  if (goal === 'lose_fat') {
    pPct = 0.35; cPct = 0.40; fPct = 0.25; // Higher protein, lower carbs
  } else if (goal === 'gain_muscle') {
    pPct = 0.30; cPct = 0.45; fPct = 0.25; // High carb/protein mix
  } else if (goal === 'endurance') {
    pPct = 0.20; cPct = 0.55; fPct = 0.25; // Extra carbs for energy
  }

  // 1g Protein = 4 kcal, 1g Carb = 4 kcal, 1g Fat = 9 kcal
  const targetProtein = Math.round((targetCalories * pPct) / 4);
  const targetCarbs = Math.round((targetCalories * cPct) / 4);
  const targetFats = Math.round((targetCalories * fPct) / 9);

  return {
    bmi: Math.round(bmi * 10) / 10,
    bmr: Math.round(bmr),
    tdee: Math.round(tdee),
    targetCalories: Math.round(targetCalories),
    targetProtein,
    targetCarbs,
    targetFats
  };
}


