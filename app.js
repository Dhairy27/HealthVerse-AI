// Simulation scanning logic
function scanMealImage(filename, callback) {
  // Guess the food based on the filename/metadata keywords
  const fileLower = (filename || "").toLowerCase();
  let foodKey = 'fallback';

  if (fileLower.includes('salad') || fileLower.includes('caesar') || fileLower.includes('green')) {
    foodKey = 'salad';
  } else if (fileLower.includes('burger') || fileLower.includes('fry') || fileLower.includes('fries') || fileLower.includes('beef') || fileLower.includes('junk')) {
    foodKey = 'burger';
  } else if (fileLower.includes('salmon') || fileLower.includes('fish') || fileLower.includes('quinoa') || fileLower.includes('teriyaki')) {
    foodKey = 'salmon';
  } else if (fileLower.includes('pizza') || fileLower.includes('pepperoni') || fileLower.includes('slice')) {
    foodKey = 'pizza';
  } else if (fileLower.includes('toast') || fileLower.includes('avocado') || fileLower.includes('egg')) {
    foodKey = 'toast';
  } else {
    // Random select logic from presets for cool generic results if no keyword
    const keys = ['salad', 'burger', 'salmon', 'pizza', 'toast'];
    foodKey = keys[Math.floor(Math.random() * keys.length)];
  }

  const result = FOOD_DATABASE[foodKey];
  
  // Phase loading simulation
  const statuses = [
    "Analyzing image structure...",
    "Running computer vision models...",
    "Segmenting food items...",
    "Detecting nutritional characteristics...",
    "Querying HealthVerse AI knowledge..."
  ];

  let currentStep = 0;
  
  const timer = setInterval(() => {
    if (currentStep < statuses.length) {
      if (typeof callback.onProgress === 'function') {
        callback.onProgress(statuses[currentStep]);
      }
      currentStep++;
    } else {
      clearInterval(timer);
      if (typeof callback.onComplete === 'function') {
        callback.onComplete(result);
      }
    }
  }, 500); // 2.5 seconds total
}


// --- ACTIVE WORKOUT CONTROLLER ---

let workoutTimerInterval = null;
let workoutSeconds = 0;

function startWorkoutTimer(onTick) {
  workoutSeconds = 0;
  if (workoutTimerInterval) clearInterval(workoutTimerInterval);
  workoutTimerInterval = setInterval(() => {
    workoutSeconds++;
    const hours = Math.floor(workoutSeconds / 3600);
    const minutes = Math.floor((workoutSeconds % 3600) / 60);
    const secs = workoutSeconds % 60;
    const formatted = [
      hours > 0 ? String(hours).padStart(2, '0') : null,
      String(minutes).padStart(2, '0'),
      String(secs).padStart(2, '0')
    ].filter(Boolean).join(':');
    
    if (typeof onTick === 'function') {
      onTick(formatted);
    }
  }, 1000);
}

function stopWorkoutTimer() {
  if (workoutTimerInterval) {
    clearInterval(workoutTimerInterval);
    workoutTimerInterval = null;
  }
  return Math.ceil(workoutSeconds / 60); // Return duration in minutes
}


// --- CHARTS VISUALIZATION BINDINGS (CHART.JS) ---

let weightChartInstance = null;
let calorieChartInstance = null;

function renderCharts() {
  const ctxWeight = document.getElementById('weightHistoryChart');
  const ctxCalorie = document.getElementById('calorieHistoryChart');

  if (!ctxWeight || !ctxCalorie) return;

  // Destruct existing instances
  if (weightChartInstance) weightChartInstance.destroy();
  if (calorieChartInstance) calorieChartInstance.destroy();

  // Sort weight logs by date
  const sortedWeightLogs = [...appState.weightLogs].sort((a, b) => new Date(a.date) - new Date(b.date));
  const weightLabels = sortedWeightLogs.map(l => {
    const d = new Date(l.date);
    return d.toLocaleDateString(undefined, { month: 'short', day: 'numeric' });
  });
  const weightData = sortedWeightLogs.map(l => l.weight);

  const isDark = appState.theme === 'dark';
  const gridColor = isDark ? 'rgba(255, 255, 255, 0.05)' : 'rgba(0, 0, 0, 0.05)';
  const textColor = isDark ? '#9ca3af' : '#64748b';
  const legendColor = isDark ? '#f4f4f5' : '#1e293b';

  // Weight Trend Line Chart
  weightChartInstance = new Chart(ctxWeight, {
    type: 'line',
    data: {
      labels: weightLabels.length ? weightLabels : ["No logs"],
      datasets: [{
        label: 'Weight (kg)',
        data: weightData.length ? weightData : [0],
        borderColor: isDark ? '#06b6d4' : '#0d9488',
        backgroundColor: isDark ? 'rgba(6, 182, 212, 0.15)' : 'rgba(13, 148, 136, 0.1)',
        borderWidth: 3,
        fill: true,
        tension: 0.4,
        pointBackgroundColor: isDark ? '#8b5cf6' : '#4f46e5',
        pointHoverRadius: 7
      }]
    },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      plugins: {
        legend: { display: false }
      },
      scales: {
        y: {
          grid: { color: gridColor },
          ticks: { color: textColor }
        },
        x: {
          grid: { display: false },
          ticks: { color: textColor }
        }
      }
    }
  });

  // Calculate past 7 days calories logs vs goal line
  const now = new Date();
  const pastDaysLabels = [];
  const calorieIntakes = [];
  const targetCaloriesArray = [];
  const dailyTarget = appState.profile ? appState.profile.metrics.targetCalories : 2000;

  for (let i = 6; i >= 0; i--) {
    const d = new Date();
    d.setDate(now.getDate() - i);
    const dayName = d.toLocaleDateString(undefined, { weekday: 'short' });
    pastDaysLabels.push(dayName);

    // Sum matching meals for that historical day. Since it's mockup, we generate realistic values
    // if there are no logged meals in historical records.
    let dailySum = 0;
    const dateStr = d.toDateString();
    
    if (dateStr === now.toDateString()) {
      dailySum = appState.dailyIntake.calories;
    } else {
      // Return a seeded pseudo random calorie total that centers around targets for realistic visual layout
      const seedVal = Math.sin(d.getDate()) * 200;
      dailySum = Math.round(dailyTarget * 0.9 + seedVal);
    }
    calorieIntakes.push(dailySum);
    targetCaloriesArray.push(dailyTarget);
  }

  // Calorie History Bar Chart
  calorieChartInstance = new Chart(ctxCalorie, {
    type: 'bar',
    data: {
      labels: pastDaysLabels,
      datasets: [
        {
          label: 'Calories Consumed',
          data: calorieIntakes,
          backgroundColor: isDark ? '#8b5cf6' : '#4f46e5',
          borderRadius: 6,
          barThickness: 16
        },
        {
          label: 'Target Limit',
          data: targetCaloriesArray,
          type: 'line',
          borderColor: '#ec4899',
          borderWidth: 2,
          borderDash: [5, 5],
          fill: false,
          pointStyle: 'none',
          pointRadius: 0
        }
      ]
    },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      plugins: {
        legend: {
          labels: { color: legendColor }
        }
      },
      scales: {
        y: {
          grid: { color: gridColor },
          ticks: { color: textColor }
        },
        x: {
          grid: { display: false },
          ticks: { color: textColor }
        }
      }
    }
  });
}


